export const fuelCostPerTon = 650; // USD per ton
export const co2CostPerTon = 85; // USD per ton (carbon tax estimate)

export function calculateSavings(bioScore: number, currentConsumption: number): { fuelSaved: number; moneySaved: number; co2Reduced: number } {
  const additionalConsumption = (bioScore / 100) * 0.15 * currentConsumption;
  const fuelSaved = additionalConsumption * 30; // Monthly savings
  const moneySaved = fuelSaved * fuelCostPerTon;
  const co2Reduced = fuelSaved * 3.14; // CO2 factor for marine fuel

  return { fuelSaved, moneySaved, co2Reduced };
}

export function getRecommendedCleaningDate(vessel: Vessel): string {
  const bioScoreThreshold = 60;
  const daysUntilThreshold = Math.max(0, Math.round((bioScoreThreshold - vessel.bioScore) / 2));
  const recommendedDate = new Date();
  recommendedDate.setDate(recommendedDate.getDate() + daysUntilThreshold);
  return recommendedDate.toISOString().split('T')[0];
}

